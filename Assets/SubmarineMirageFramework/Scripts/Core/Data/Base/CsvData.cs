namespace SubmarineMirage.Data {
	using System.Collections.Generic;
	///====================================================================================================
	/// <summary>
	/// ■ CSV情報のクラス
	///----------------------------------------------------------------------------------------------------
	///		CSVを利用する各種情報クラスは、このクラスを継承する。
	/// </summary>
	///====================================================================================================
	public abstract class CSVData<T> : BaseData {
		///------------------------------------------------------------------------------------------------
		/// ● 要素
		///------------------------------------------------------------------------------------------------
		/// <summary>辞書への登録鍵</summary>
		public abstract T _registerKey { get; }
		///------------------------------------------------------------------------------------------------
		/// <summary>
		/// ● コンストラクタ
		/// </summary>
		///------------------------------------------------------------------------------------------------
		public CSVData() {
		}
		///------------------------------------------------------------------------------------------------
		/// <summary>
		/// ● 設定
		/// </summary>
		///------------------------------------------------------------------------------------------------
		public abstract void Set( string fileName, int index, List<string> texts );
	}
}