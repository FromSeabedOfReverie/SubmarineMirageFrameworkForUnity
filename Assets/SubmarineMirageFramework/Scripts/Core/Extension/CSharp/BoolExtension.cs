namespace SubmarineMirage.Extension {
	///====================================================================================================
	/// <summary>
	/// ■ 真偽値の拡張クラス
	///----------------------------------------------------------------------------------------------------
	/// </summary>
	///====================================================================================================
	public static class BoolExtension {
		///------------------------------------------------------------------------------------------------
		/// <summary>
		/// ● 真偽値から整数に変換
		/// </summary>
		///------------------------------------------------------------------------------------------------
		public static int ToInt( this bool b ) {
			return b ? 1 : 0;
		}
	}
}